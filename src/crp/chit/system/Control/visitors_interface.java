    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            // TODO add your handling code here:
        //Click here to update a visitor in the database.
        row = V_Table.getSelectedRow();
        v_id = Integer.valueOf(V_Table.getValueAt(row, 0).toString());
        nic = V_Table.getValueAt(row, 1).toString();
        name = V_Table.getValueAt(row, 2).toString();
        address = V_Table.getValueAt(row, 3).toString();
        contact = Integer.valueOf(V_Table.getValueAt(row, 4).toString());
        if(nic.length()!=0 && nic.length()==10){
            if(name.length()!=0 && name.length()<=120){
                if(address.length()!=0 && address.length()<=120){
                    if(V_Table.getValueAt(row, 4).toString().length()!=0 && V_Table.getValueAt(row, 4).toString().length()==10){
                        v2 = new visitors();
                        v2.setNic(nic);
                        v2.setName(name);
                        v2.setAddress(address);
                        v2.setContact(contact);
                        result = visitors_dao.updateVisitor(v2, v_id);
                        //if the result is true/false display relavant message to the user.
                        if(result==1){
                            JOptionPane.showMessageDialog(null, "Visitor updated successfully.", "System",JOptionPane.INFORMATION_MESSAGE);
                            visitors_interface v2 = new visitors_interface(adminId);
                            v2.setVisible(true);
                            dispose();
                        }else{
                            JOptionPane.showMessageDialog(null, "Failed to update information.","System",JOptionPane.ERROR_MESSAGE);
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Enter valid contact number.","System",JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Enter valid address.","System",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Enter valid name.","System",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Enter valid NIC.","System",JOptionPane.ERROR_MESSAGE);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please select a row.","System",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

