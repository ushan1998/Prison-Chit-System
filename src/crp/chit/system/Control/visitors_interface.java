    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        //Click here to delete a visitor.
        try{
            row = V_Table.getSelectedRow();
            v_id = Integer.parseInt(V_Table.getValueAt(row, 0).toString());
            result = visitors_dao.deleteVisitor(v_id);
            //if the value of result is true/false display relavant information to the user.
            if(result==0){
                JOptionPane.showMessageDialog(null, "Failed to delete the visitor.","System",JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Visitor deleted successfully.","System",JOptionPane.INFORMATION_MESSAGE);
                visitors_interface v1 = new visitors_interface(adminId);
                v1.setVisible(true);
                dispose();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please select a row.","System",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

