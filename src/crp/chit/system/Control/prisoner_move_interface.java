    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Click here to add information about prisoner's movement.
        try{
           p_id = Integer.parseInt(prisoner_id.getText());
           m_from = p_c_p.getSelectedItem().toString(); // Prisoner's current place p_c_p.
           m_to = p_n_p.getSelectedItem().toString();   // Prisoner's new place p_n_p.
           if(prisoner_id.getText().length()!=0 && prisoner_id.getText().length()<=10){
               if(m_from!=null){
                   if(m_to!=null){
                        p1 = new prisoner_move();
                        p1.setTime(Time.valueOf(LocalTime.now()));
                        p1.setDate(Date.valueOf(LocalDate.now()));
                        p1.setM_from(m_from); 
                        p1.setM_to(m_to);     
                        p1.setPrisoner_id(p_id);
                        result = prisoner_move_dao.addPrisonerMove(p1);
                        //if the result is true/false showing the user the relavant output.
                        if(result==1){
                            //Get the prisoners information and send an email to the prisoner given mail to inform about the prisoner movement.
                            Vector v1 = prisoner_dao.serchPrisoner(Integer.parseInt(prisoner_id.getText()));
                            to = v1.get(6).toString();
                            sub = "About prisoner movement";
                            message = "Please take not that the following Prisoner "+v1.get(2).toString()+" has been moved from "+p_c_p.getSelectedItem().toString()+" to "+p_n_p.getSelectedItem().toString()+" .";
                            user = "prisonchitsystem@gmail.com";
                            pass = "123!@#qweQWE";
                            send(to,sub,message,user,pass);     //Send the email.
                            JOptionPane.showMessageDialog(null, "Information added successfully.", "System", JOptionPane.INFORMATION_MESSAGE);
                            prisoner_move_interface p2 = new prisoner_move_interface(adminId);
                            p2.setVisible(true);
                            dispose();
                        }else{
                            JOptionPane.showMessageDialog(null, "Failed to add information.", "System", JOptionPane.ERROR_MESSAGE);
                            prisoner_id.setText("");
                        }
                   }else{
                        JOptionPane.showMessageDialog(null, "Please select the place to move to.", "System", JOptionPane.ERROR_MESSAGE);
                   }
               }else{
                   JOptionPane.showMessageDialog(null, "Please select the place to move from.", "System", JOptionPane.ERROR_MESSAGE);
               }
           }else{
               JOptionPane.showMessageDialog(null, "Please enter prisoner id.", "System", JOptionPane.ERROR_MESSAGE);
               prisoner_id.setText("");
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please enter a numeric value for prisoner id.","System",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

