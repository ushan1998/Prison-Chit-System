    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Click here to update information about prisoner movemoent. 
        try{
        row = M_Table.getSelectedRow();
        p_m_id = Integer.valueOf(M_Table.getValueAt(row, 0).toString());
        m_from = M_Table.getValueAt(row, 1).toString();
        m_to = M_Table.getValueAt(row, 2).toString();
        if(m_from.length()!=0 && m_from.length()<=20){
            if(m_to.length()!=0 && m_to.length()<=10){
                    p1 = new prisoner_move();
                    p1.setM_from(M_Table.getValueAt(row, 1).toString());
                    p1.setM_to(M_Table.getValueAt(row, 2).toString());
                    result = prisoner_move_dao.updatePrisonerMove(p1, p_m_id);
                    //if result is true/false display relavant information to the user.
                    if(result==1){
                        JOptionPane.showMessageDialog(null, "Information updated successfully.","System",JOptionPane.INFORMATION_MESSAGE);
                        prisoner_move_interface p2 = new prisoner_move_interface(adminId);
                        p2.setVisible(true);
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Failed to update information.","System",JOptionPane.ERROR_MESSAGE);
                    }
            }else{
                JOptionPane.showMessageDialog(null, "Please enter a valid prisoner move to place.","System",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please enter valid prisoner move from place..","System",JOptionPane.ERROR_MESSAGE);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please select a row.","System",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //Click here to get information about the prisoner movement.
        try{
            if(P_M_S_Id.getText().length()!=0 && P_M_S_Id.getText().length()<=10){
                p_m_id = Integer.parseInt(P_M_S_Id.getText());
                v1 = prisoner_move_dao.getPrisonerMoveInfo(p_m_id);
                if(v1==null){
                    JOptionPane.showMessageDialog(null, "Enter valid id.","System",JOptionPane.ERROR_MESSAGE);
                }else{
                    M_Table.setModel(new DefaultTableModel(null,new String[]{"Move Id","Moved from","Moved to","Time","Date","Prisoner Id"}));
                    model = (DefaultTableModel) M_Table.getModel();
                    for(int i =0; i<v1.size(); i++){
                        model.addRow((Vector) v1.get(i));
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null, "Please enter valid id.","System",JOptionPane.ERROR_MESSAGE);
                P_M_S_Id.setText("");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please enter a numeric value.","System",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

